    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
           Class.forName("com.mysql.jdbc.Driver");
	Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cms", "root", "root");
        PreparedStatement p = con.prepareStatement("update faculty set fname=?, phone_no=?, address=?, email_id=?, cname=? ,salary=? where fid=?;");
        String sidd = jTextField1.getText();
        String snm = jTextField2.getText();
        String phn = jTextField3.getText();
        String ad = jTextField4.getText();
        String eid = jTextField5.getText();
        String cnm = jTextField6.getText();
        String sal=jTextField7.getText();
        
        p.setString(7, sidd);
        p.setString(1, snm);
        p.setString(2, phn);
        p.setString(3, ad);
        p.setString(4, eid);
        p.setString(5, cnm);
        p.setString(6,sal);
        int i=p.executeUpdate();
        if(i==0) {
            JOptionPane.showMessageDialog(null, "Record not updated");
        }
        else{
            JOptionPane.showMessageDialog(null, "Record updated");
        }
        con.close();
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");
        jTextField7.setText("");
        jTextField4.setEditable(false);
        jTextField5.setEditable(false);
        jTextField6.setEditable(false);
        jTextField3.setEditable(false);
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try{
           Class.forName("com.mysql.jdbc.Driver");
	Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cms", "root", "root");
	PreparedStatement ps = con.prepareStatement("select * from faculty");
        ResultSet rs = ps.executeQuery();
        String fidd = jTextField1.getText();
        String fnm = jTextField2.getText();
        while(rs.next()) {
            if(fidd.equals(rs.getString(1)) && fnm.equals(rs.getString(2))) {
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(5));
                jTextField5.setText(rs.getString(6));
                jTextField6.setText(rs.getString(7));
                jTextField7.setText(rs.getString(8));
                
                jTextField3.setEditable(true);
                jTextField4.setEditable(true);
                jTextField5.setEditable(true);
                jTextField6.setEditable(true);
            }
        }
        String phn = jTextField3.getText();
        if(phn.equals("")) {
            JOptionPane.showMessageDialog(null, "Record not found");
        }
        
        con.close();
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

